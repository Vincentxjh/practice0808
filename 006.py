import math
print('1251+3950的结果是（以货币形式显示）：￥{:,.2f}元'.format(1251+3950))   #以货币形式表示（逗号表示以逗号分隔的数字格式）
print('{0:.1f}用科学计数法表示：{0:E}'.format(120000.1))   #用科学计数法表示
print('π取5位小数：{:.5f}'.format(math.pi))   #输出小数点后五位
print('{0:d}的十六进制结果是：{0:#x}'.format(100))   #输出十六进制数
print('天才是由{:.0%}的灵感，加上{:.0%}的汗水。'.format(0.01,0.99))   #输出百分比且不带小数